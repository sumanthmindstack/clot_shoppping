class GetKycDetailsResponseModel extends GetKycDetailsEntity {
  GetKycDetailsResponseModel({
    required super.data,
  });

  factory GetKycDetailsResponseModel.fromJson(Map<String, dynamic> json) {
    return GetKycDetailsResponseModel(
      data: json['data'] != null
          ? KycDetailsModel.fromJson(json['data'])
          : KycDetailsModel.empty(),
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'data': data,
    };
  }
}

